package com.example.spring_multi_ds_config.entity.primary;

import jakarta.persistence.*;

@Entity
@Table(name = "users")
public class PrimaryUser {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}